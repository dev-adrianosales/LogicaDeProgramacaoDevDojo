public class ReutilizandoVariaveis {
    public static void main(String[] args) {
        double salario = 5000;
        double resultado = salario * 30 / 100;
        System.out.println("30% do Salário é: "+resultado);

        resultado = salario * 15 / 100;
        System.out.println("30% do Salário é: "+resultado);


        resultado = salario * 5 / 100;
        System.out.println("30% do Salário é: "+resultado);

    }
}
